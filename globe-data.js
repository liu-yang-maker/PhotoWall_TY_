function buildTravelCities() {
    const data = (typeof timelineData !== 'undefined') ? timelineData : window.timelineData;
    if (!data) return {};
    const map = {};
    data.forEach(item => {
        if (!item.city) return;
        if (!map[item.city]) map[item.city] = { events: [] };
        map[item.city].events.push({ dateKey: item.dateKey, title: item.title });
    });
    return map;
}
